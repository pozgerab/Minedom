package com.gertoxq.minedom.StatSystem;

/**
 * Represents the state of an entity
 */
public enum EntityState {

    /**
     * Passive entity
     */
    PASSIVE,
    /**
     * Aggressive entity
     */
    ACTIVE,
    /**
     * Player entity
     */
    PLAYER,
    /**
     * Persistent marker entity
     */
    INDICATOR

}
